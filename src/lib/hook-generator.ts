/**
 * Generate project structure update section for pre-commit hook
 * @returns Bash script section for project structure update
 */
function generateProjectStructureSection(): string {
  return `
# Update project structure in .claude/project-structure.md
# This happens for ALL branches (main and worktrees)
CHANGED_FILES=$(git diff --cached --name-status | grep -E '^[AD]' || true)

if [ -n "$CHANGED_FILES" ]; then
    printf "\${YELLOW}Updating project structure in .claude/project-structure.md...\${NC}\\n"

    # Run the update script if it exists
    if [ -f "scripts/update-project-structure.js" ]; then
        node scripts/update-project-structure.js

        # Stage the updated project-structure.md if it was modified
        if git diff --name-only | grep -q ".claude/project-structure.md"; then
            git add .claude/project-structure.md
            printf "\${GREEN}✅ Project structure updated and staged\${NC}\\n"
        fi
    fi
fi
`;
}


