#!/usr/bin/env node

// src/templates/scripts/update-project-structure.ts
import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
var PROJECT_STRUCTURE_FILE = ".claude/project-structure.md";
var IGNORED_DIRS = /* @__PURE__ */ new Set([
  ".cache",
  ".DS_Store",
  ".git",
  ".next",
  ".turbo",
  ".vercel",
  "build",
  "coverage",
  "dist",
  "node_modules",
  "out"
]);
var IGNORED_FILES = /* @__PURE__ */ new Set([
  "*.lock",
  "*.log",
  ".DS_Store",
  "package-lock.json",
  "pnpm-lock.yaml",
  "Thumbs.db",
  "yarn.lock"
]);
var MAX_DEPTH = 4;
function generateProjectStructure(dir = ".", prefix = "", _isLast = true, depth = 0, maxDepth = MAX_DEPTH) {
  if (depth > maxDepth) return "";
  let structure = "";
  const items = fs.readdirSync(dir).toSorted((a, b) => {
    const aIsDir = fs.statSync(path.join(dir, a)).isDirectory();
    const bIsDir = fs.statSync(path.join(dir, b)).isDirectory();
    if (aIsDir && !bIsDir) return -1;
    if (!aIsDir && bIsDir) return 1;
    return a.localeCompare(b);
  });
  const filteredItems = items.filter((item) => {
    const itemPath = path.join(dir, item);
    return !shouldIgnore(itemPath, item);
  });
  for (let index = 0; index < filteredItems.length; index += 1) {
    const item = filteredItems[index];
    if (item === void 0) continue;
    const itemPath = path.join(dir, item);
    const isLastItem = index === filteredItems.length - 1;
    const stats = fs.statSync(itemPath);
    const connector = isLastItem ? "\u2514\u2500\u2500 " : "\u251C\u2500\u2500 ";
    const extension = isLastItem ? "    " : "\u2502   ";
    if (stats.isDirectory()) {
      structure += `${prefix}${connector}${item}/
`;
      structure += generateProjectStructure(
        itemPath,
        prefix + extension,
        isLastItem,
        depth + 1,
        maxDepth
      );
    } else {
      let description = "";
      if (item === "package.json") description = " # Project configuration";
      if (item === "CLAUDE.md") description = " # Claude instructions (auto-managed)";
      if (typeof item === "string" && (item.endsWith(".config.js") || item.endsWith(".config.ts"))) {
        description = " # Configuration file";
      }
      structure += `${prefix}${connector}${item}${description}
`;
    }
  }
  return structure;
}
function shouldIgnore(itemPath, itemName) {
  if (IGNORED_DIRS.has(itemName)) return true;
  for (const pattern of IGNORED_FILES) {
    if (pattern.includes("*")) {
      const regex = new RegExp(pattern.replace("*", ".*"));
      if (regex.test(itemName)) return true;
    } else if (pattern === itemName) {
      return true;
    }
  }
  try {
    execSync(`git check-ignore "${itemPath}"`, { stdio: "pipe" });
    return true;
  } catch {
    return false;
  }
}
function updateProjectStructure() {
  const structureFile = path.join(process.cwd(), PROJECT_STRUCTURE_FILE);
  const claudeDir = path.dirname(structureFile);
  if (!fs.existsSync(claudeDir)) {
    fs.mkdirSync(claudeDir, { recursive: true });
  }
  const newStructure = generateProjectStructure();
  const content = `# Project Structure

Auto-generated project structure (updated on commit).

\`\`\`
${newStructure}\`\`\`

<!-- This file is auto-generated by pre-commit hook. Do not edit manually. -->
`;
  fs.writeFileSync(structureFile, content);
  console.log(`\u2705 Updated project structure in ${PROJECT_STRUCTURE_FILE}`);
}
if (process.argv[1] === new URL(import.meta.url).pathname) {
  updateProjectStructure();
}
